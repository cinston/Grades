var marks = parseInt(prompt("Enter marks"));
if (marks <=100 && marks >=80) {
  alert("Grade A");
}
else if (marks <=79 && marks >=60) {
  alert ("Grade B");
}
else if (marks <=59 && marks >=40) {
  alert ("Grade C");
}
else if (marks <=39 && marks >=20) {
  alert("Grade D");
}
else if (marks <=20 && marks >=0) {
  alert("Grade E")
}
